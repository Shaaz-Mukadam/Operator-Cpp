//************Relation Operator************
//Relation operator is used to comparision of value of two operand.
#include<iostream>
using namespace std;
//true 0 false 1
int main() {
 int a = 10 , b = 20 ;
 cout<<(a==b)<<endl;
 cout<<(a>b)<<endl;
 cout<<(a<b)<<endl;
 cout<<(a!=b)<<endl;
}
