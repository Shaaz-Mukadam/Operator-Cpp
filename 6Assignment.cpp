//*******************Assignment OPERATOR********
//Assignment operator is used to assigning a value
//They are = , += , -= , *= , /=;
#include<iostream>
using namespace std;

int main() {
  int a = 2 , b = 7 ;
  a+=b;    //as similar as a = a +b;
  cout<<a<<endl;

  int c = 9 , d  = 8;
  c-=d;   //as similar as b = b - d;
  cout<<c<<endl;

  int e = 10 , f = 10 ;
  e*=f;
  cout<<e;

  //It will work similar to others.
}
